#! /usr/bin/env ruby


####################################################################
require 'getoptlong'


####################################################################
infile = nil


####################################################################
opts = GetoptLong.new(
  ['-i', GetoptLong::REQUIRED_ARGUMENT],
)


opts.each do |opt, value|
  case opt
    when '-i'
      infile = value
  end
end


####################################################################
in_fh = File.open(infile, 'r')
